package model;

/**
 *
 * @author felipe
 */
public class Language extends Json {
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    @Override
    public String serialize() {
        String json = "{ \"name\": \"" + name + "\" }";
        return json;
    }
}
